    private void jBtnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnLoginActionPerformed
        if(jTxtCPF.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Digite o login");
        }
        else if(jTxtPassword.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Digite a senha");
        }
        else
        {
            User user = new User();
            user.setCpf(jTxtCPF.getText());
            user.setPassword(String.valueOf(jTxtPassword.getPassword()));
            try
            {
                User u = user.login(user);
                if(u != null)
                {
                    JFGuiHoteleiroMain jfm = new JFGuiHoteleiroMain();
                    jfm.load_user(u);
                    this.dispose();
                    jfm.show();                    
                }
                else
                    JOptionPane.showMessageDialog(null, "Login e/ou senha incorreto(s)");
            }
            catch (SQLException | IOException ex) 
            {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_jBtnLoginActionPerformed

